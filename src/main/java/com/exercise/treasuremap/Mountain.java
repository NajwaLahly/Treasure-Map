package com.exercise.treasuremap;

public class Mountain {
    private int posX;
    private int posY;

    public Mountain(int posX, int posY) {
        this.posX = posX;
        this.posY = posY;
    }

    public int getPosX() {
        return posX;
    }

    public int getPosY() {
        return posY;
    }
}
